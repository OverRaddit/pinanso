#include <unistd.h>

int	ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	main(int argc, char *argv[])
{
	if (argc != 1)
		return (-1);
	write(1, argv[0], ft_strlen(argv[0]));
	return (0);
}
