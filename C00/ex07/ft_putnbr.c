#include <unistd.h>

void    ft_putnbr(int nb)
{
    int i=0;
    char buf[11];
    while(nb>0)
    {
        buf[i++] = (char)(nb%10+48);
        nb = nb/10;
    }
    while(i>=0)
    {
        write(1,&buf[--i],1);
    }
    
}

int main(){
    ft_putnbr(1147483647);
}